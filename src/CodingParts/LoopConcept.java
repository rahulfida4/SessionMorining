package CodingParts;

public class LoopConcept {

	public static void main(String[] args) {
		//1.While loop.
		
		// dis-advantages of while loop it will give an infinite loop if u don't give incremental / decremental parts.
		
		int i=1;  // initialization
		while(i<=10){//conditional
			System.out.println(i);
			i++;//incremental parts
		}
	}

}
